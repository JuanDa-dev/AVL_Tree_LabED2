    private void txtDatoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDatoKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!Character.isDigit(c)) {
            evt.consume();
        }
    }//GEN-LAST:event_txtDatoKeyTyped

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if (!txtDato.getText().equals("")) {
            if (tree.search(Integer.parseInt(txtDato.getText()))) {
                JOptionPane.showMessageDialog(null, "Sí se encontró el nodo");
            } else {
                JOptionPane.showMessageDialog(null, "NO se encontró el nodo");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Verifique el dato");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnalturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnalturaActionPerformed
        // TODO add your handling code here:
        if (!txtDato.getText().equals("")) {
            resultado = "";
            if (tree.search(Integer.parseInt(txtDato.getText()))) {
                resultadotxt.append("\nLa altura del nodo " + txtDato.getText() + " es: " + resultado);
            } else {
                JOptionPane.showMessageDialog(null, "NO se encontró el nodo");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Verifique el dato");
        }
    }//GEN-LAST:event_btnalturaActionPerformed

